def ask(question, required=True, default=""):
    """Ask a single question. Keep asking until they answer if required."""
    while True:
        if default:
            answer = input(f"  {question} [{default}]: ").strip()
            if not answer:
                return default
        else:
            answer = input(f"  {question}: ").strip()
        if answer or not required:
            return answer
        print("  ^ This one's required. Try again.\n")


def ask_yes_no(question, default=None):
    """Ask a yes/no question."""
    suffix = " (y/n)"
    if default is True:
        suffix = " (Y/n)"
    elif default is False:
        suffix = " (y/N)"

    while True:
        answer = input(f"  {question}{suffix}: ").strip().lower()
        if not answer and default is not None:
            return default
        if answer in ("y", "yes"):
            return True
        if answer in ("n", "no"):
            return False
        print("  ^ Type 'y' or 'n'.\n")


def ask_choice(question, options):
    """Ask them to pick from a numbered list. Returns the option dict."""
    print(f"  {question}\n")
    for i, opt in enumerate(options, 1):
        print(f"    {i}. {opt['label']}")
    print()

    while True:
        answer = input("  Enter number: ").strip()
        try:
            idx = int(answer)
            if 1 <= idx <= len(options):
                return options[idx - 1]
        except ValueError:
            pass
        print(f"  ^ Pick a number between 1 and {len(options)}.\n")


def ask_multi_choice(question, options):
    """Ask them to pick one or more from a numbered list. Returns list of option dicts."""
    print(f"  {question}\n")
    for i, opt in enumerate(options, 1):
        print(f"    {i}. {opt['label']}")
    print()
    print("  You can pick more than one. Example: 1,3")
    print()

    while True:
        answer = input("  Enter number(s): ").strip()
        try:
            picks = [int(x.strip()) for x in answer.split(",")]
            if all(1 <= p <= len(options) for p in picks):
                return [options[p - 1] for p in picks]
        except ValueError:
            pass
        print(f"  ^ Enter numbers between 1 and {len(options)}, separated by commas.\n")


def run_cmd(cmd, check=True):
    """Run a shell command and return output."""
    result = subprocess.run(cmd, shell=True, capture_output=True, text=True)
    if check and result.returncode != 0:
        return None
    return result.stdout.strip()


def save_env(config):
    """Write the .env file from config dict."""
    lines = ["# Generated by setup.py — do NOT commit this file\n"]
    for key, value in config.items():
        lines.append(f"{key}={value}")
    ENV_FILE.write_text("\n".join(lines) + "\n")


