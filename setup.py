def step_content_source():
    banner("Step 2: Where Does Your Content Come From?")
    print("  The pipeline needs source material to turn into podcast episodes.")
    print("  This could be articles on your website, YouTube videos, an RSS feed,")
    print("  or you can just type in topics and it'll research them.\n")

    sources = ask_multi_choice(
        "Where should it pull content from?",
        [
            {"label": "My website — it'll scrape articles/pages", "value": "website"},
            {"label": "My YouTube channel — it'll use video transcripts", "value": "youtube"},
            {"label": "An RSS feed — blog or news feed", "value": "rss"},
            {"label": "I'll provide topics — it'll research and write from scratch", "value": "manual"},
        ],
    )

    config = {}
    source_types = [s["value"] for s in sources]
    config["CONTENT_SOURCE_TYPES"] = ",".join(source_types)

    if "website" in source_types:
        print()
        url = ask("What's your website URL? (e.g., https://example.com)")
        config["WEBSITE_URL"] = url

        print()
        print("  How should it find content on your site?\n")
        crawl = ask_choice(
            "Pick one:",
            [
                {"label": "Scrape the whole site (finds all pages/articles automatically)", "value": "full"},
                {"label": "Just one section (e.g., /blog or /help)", "value": "section"},
                {"label": "I'll give it a sitemap URL", "value": "sitemap"},
            ],
        )
        config["WEBSITE_CRAWL_MODE"] = crawl["value"]

        if crawl["value"] == "section":
            print()
            section = ask("What section? (e.g., /blog, /articles, /help)")
            config["WEBSITE_SECTION"] = section
        elif crawl["value"] == "sitemap":
            print()
            sitemap = ask("What's the sitemap URL? (e.g., https://example.com/sitemap.xml)")
            config["WEBSITE_SITEMAP_URL"] = sitemap

    if "youtube" in source_types:
        print()
        yt_url = ask("What's your YouTube channel URL?")
        config["YOUTUBE_CHANNEL_URL"] = yt_url

    if "rss" in source_types:
        print()
        rss_url = ask("What's the RSS feed URL?")
        config["RSS_FEED_URL"] = rss_url

    if "manual" in source_types:
        print()
        print("  No problem — you can add topics later in data/topics.json")
        print("  or pass them on the command line: python3 run.py --topic \"Your topic here\"")

    print()
    print(f"  Content sources configured: {', '.join(source_types)}")
    print()
    input("  Press Enter to continue...")

    return config


