def step_install_deps():
    banner("Step 10: Installing Dependencies")
    print("  Setting up Python virtual environment and installing packages...\n")

    venv_dir = BASE_DIR / "venv"

    if not venv_dir.exists():
        print("  Creating virtual environment...")
        subprocess.run([sys.executable, "-m", "venv", str(venv_dir)], check=True)
        print("  Done.\n")

    pip = venv_dir / "bin" / "pip"
    if os.name == "nt":
        pip = venv_dir / "Scripts" / "pip"

    req_file = BASE_DIR / "requirements.txt"
    if req_file.exists():
        print("  Installing packages (this may take a minute)...\n")
        subprocess.run([str(pip), "install", "-r", str(req_file)], check=True)
        print("\n  All packages installed.\n")
    else:
        print("  No requirements.txt found — skipping.\n")

    input("  Press Enter to continue...")
    return {}


